    public final JMethod getMethod() {
        final JMethod method = new JMethod();
        
        method.setName(jTextFieldName.getText());
        method.setDescription(jTextAreaDesc.getText());
        method.setAccessKeyword(JAccessKeyword.valueOf(
                (String) jComboBoxAccessibility.getSelectedItem()));
        method.setImplementationKeyword(JImplementationKeyword.valueOf(
                (String) jComboBoxKeyword.getSelectedItem()));
        
        method.getReturnType().setDataType(JDataType.getFromName(
                (String) jComboBoxReturn.getSelectedItem()));
        method.getReturnType().setCustomDataType(
                jTextFieldCustomReturn.getText());
        
        return method;
    }
    

    private void jButtonAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAcceptActionPerformed
        if (jTextFieldName.getText() == null || jTextFieldName.getText().isEmpty()) {
            setAvailableName(false);
        }
        
        if (validName) {
            if (onAcceptCallback != null) {
                onAcceptCallback.run();
            }

            closeDialog(null);
        }
    }//GEN-LAST:event_jButtonAcceptActionPerformed

    private void jTextFieldNameCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTextFieldNameCaretUpdate
        if (onChangeNameCallback != null) {
            onChangeNameCallback.accept(jTextFieldName.getText());
        }
    }//GEN-LAST:event_jTextFieldNameCaretUpdate

    private void jComboBoxReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxReturnActionPerformed
        if (jComboBoxReturn.getSelectedItem() == JDataType.CUSTOM.getName()) {
            jTextFieldCustomReturn.setVisible(true);
        } else {
            jTextFieldCustomReturn.setVisible(false);
        }
    }//GEN-LAST:event_jComboBoxReturnActionPerformed

